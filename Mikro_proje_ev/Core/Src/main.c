void perdeAc(){
		TIM3->CCR1=50; 
		HAL_Delay(2000);
} //manuel açma kapamalar için.

void perdeKapa(){
			TIM3->CCR1=100; //9'dan sonra 180 derece
			HAL_Delay(2000);
}

void isikKontrol(){
		HAL_ADC_PollForConversion(&hadc1,1000); //LDR okudugu veri bozulmasin diye eoc flag kontrolü yapiyoruz.
    lightValue = HAL_ADC_GetValue(&hadc1);
		
		if(lightValue<350){ //5'e kadar Servo 0 konumunda 
		perdeAc();
		}
		else if (lightValue>350 && lightValue <1800 ){
		TIM3->CCR1=75; //5 ten 9 a kadar 90 derecede 
		HAL_Delay(2000);
		}
		else if(lightValue > 1800){ //9'dan sonra 180 derecede.
		perdeKapa();
		}
}

void suKontrol(){
			if(flag==1){
			HAL_GPIO_TogglePin(GPIOB,GPIO_PIN_5);//BUZZER TETIKLENECEK.
			HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_8);//Acil durum isigi 1 yan sön yapacak.
			HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_7);//Acil durum isigi 2 yan sön yapacak.
			HAL_Delay(3000);
		}	
		else{
			HAL_GPIO_WritePin(GPIOB,GPIO_PIN_5,GPIO_PIN_RESET);//BUZZER susacak.
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_8,GPIO_PIN_RESET);//Acil durum isigi 1 sönecek.
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_7,GPIO_PIN_RESET);//Acil durum isigi 2 sönecek.
			}
			HAL_Delay(400);
}


void gazKontrol(){
	    static uint32_t adc_value = 0; // Gaz sensöründen ADC okuma

	    // Gaz sensöründen deger okumak için ADC kullanimi
	    HAL_ADC_Start(&hadc2);
	    HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
	    adc_value = HAL_ADC_GetValue(&hadc2);

	    if (adc_value > GAS_THRESHOLD){ // Eger gaz seviyesi esigin üzerindeyse
	        // Kapi Servo motoru 90 dereceye döndür
	        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, SERVO_ANGLE_90);
	        
					//perdeyi açacak.
						perdeAc();
	        // LED'i yak
	        for (int i = 0; i < 40; i++) 
	                {
	                    HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_8); // LED
											HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_7); // 2. LED
	                    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_5); // Buzzer
	                    DELAY_MS(500); 
	                }
	    }
	    else{
	        //Kapi Servo motoru 0 dereceye döndür
	        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, SERVO_ANGLE_0);
					perdeKapa();
	        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_8, GPIO_PIN_RESET);
					HAL_GPIO_WritePin(GPIOC, GPIO_PIN_7, GPIO_PIN_RESET);
	        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET);
	    }
	}




